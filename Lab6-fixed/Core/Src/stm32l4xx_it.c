/* USER CODE BEGIN 1 */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	if (LL_TIM_IsActiveFlag_CC1(TIM2))
	{
		LL_TIM_ClearFlag_CC1(TIM2);
		uint32_t capture = LL_TIM_IC_GetCaptureCH1(TIM2);
		if (!g_capture_started)
		{
			g_last_capture = capture;
			g_capture_started = 1;
	        return;
		}
		else
		{
			g_period_ticks = capture - g_last_capture;
			g_last_capture = capture;
		//if (g_period_ticks != 0) g_capture_ready = 1;
			g_capture_ready = 1;
	    }
	}
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}
void USART2_IRQHandler(void)
{
    if (LL_USART_IsActiveFlag_RXNE(USART2) && LL_USART_IsEnabledIT_RXNE(USART2))
    {
        uint8_t ch = (uint8_t)LL_USART_ReceiveData8(USART2);
        rxbuf_push(&g_rxbuf, ch);
    }

    if (LL_USART_IsActiveFlag_ORE(USART2))
    {
        LL_USART_ClearFlag_ORE(USART2);
    }

    HAL_UART_IRQHandler(&huart2);
}
