/**
  * @brief  The application entry point.
  * @retval int
  */

extern UART_HandleTypeDef huart2;

char rx_char;
char cmd_buffer[32];
int cmd_index = 0;

void process_uart(void)
{
    if (HAL_UART_Receive(&huart2, (uint8_t*)&rx_char, 1, 10) == HAL_OK)
    {
        HAL_UART_Transmit(&huart2, (uint8_t*)&rx_char, 1, 100);

        if (rx_char == '\r' || rx_char == '\n')
        {
            cmd_buffer[cmd_index] = '\0';

            if (strcmp(cmd_buffer, "sample") == 0)
                cmd_sample();
            else if (strcmp(cmd_buffer, "log") == 0)
                cmd_log();
            else if (strcmp(cmd_buffer, "data") == 0)
                cmd_data();
            else if (strcmp(cmd_buffer, "erase") == 0)
                cmd_erase();
            else
            {
                char msg[] = "Invalid\r\n";
                HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 100);
            }

            cmd_index = 0;
        }
        else
        {
            if (cmd_index < 31)
                cmd_buffer[cmd_index++] = rx_char;
        }
    }
}

